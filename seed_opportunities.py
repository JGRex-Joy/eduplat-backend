def seed_opportunities():
    from app.database import SessionLocal, Base, engine
    from app.models.opportunity import Opportunity

    db = SessionLocal()
    try:
        added = 0
        for o in OPPORTUNITIES:
            exists = db.query(Opportunity).filter_by(title=o["title"], type=o["type"]).first()
            if not exists:
                db.add(Opportunity(**o))
                added += 1
        db.commit()
        print(f"✅ Opportunities seed завершён — добавлено {added} записей")
    except Exception as e:
        db.rollback()
        print(f"❌ Ошибка: {e}")
    finally:
        db.close()


if __name__ == "__main__":
    seed_opportunities()