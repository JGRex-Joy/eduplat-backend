def seed():
    Base.metadata.create_all(bind=engine)
    db = SessionLocal()
    try:
        added = 0
        updated = 0
        for u in UNIVERSITIES:
            exists = db.query(University).filter_by(name=u["name"]).first()
            if not exists:
                db.add(University(**u))
                added += 1
            else:
                # Update full_description if missing
                if not exists.full_description:
                    exists.full_description = u["full_description"]
                    updated += 1
        db.commit()
        print(f"✅ Seed завершён — добавлено {added}, обновлено {updated} университетов")
    except Exception as e:
        db.rollback()
        print(f"❌ Ошибка: {e}")
    finally:
        db.close()


